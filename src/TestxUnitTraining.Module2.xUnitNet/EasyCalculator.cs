namespace TestxUnitTraining.Module2.xUnitNet
{
    /// <summary>
    /// A dummy class for making easy operations
    /// </summary>
    public class EasyCalculator
    {
        public static int Sum(int x, int y) => x + y;
        public static double Sum(double x, double y) => x + y;

        public static int Substract(int x, int y) => x - y;
        public static double Substract(double x, double y) => x - y;

        public static int Multiply(int x, int y) => x * y;
        public static double Multiply(double x, double y) => x * y;

        public static int Divide(int x, int y) => x / y;
        public static double Divide(double x, double y) => x / y;
    }
}